export const getRandomSpecies = (): Species => {
  return speciesData[Math.floor(Math.random() * speciesData.length)];
};

export const getSpeciesByThreshold = (threshold: number): Species | null => {
  const indices = [0, 3, 6];
  const index = indices[threshold - 1];
  return index !== undefined ? speciesData[index] : null;
};
